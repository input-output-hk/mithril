const fs = require("node:fs");
const path = require("node:path");

const explorerRoot = path.resolve(__dirname, "..");
const repositoryRoot = path.resolve(explorerRoot, "..");
const configurationDirectory = path.join(repositoryRoot, "mithril-infra", "configuration");

const outputFile = path.join(explorerRoot, "src", "genesis-verification-keys.generated.js");

const readGenesisVerificationKey = (network) => {
  const genesisVerificationKeyFile = path.join(configurationDirectory, network, "genesis.vkey");

  return fs.readFileSync(genesisVerificationKeyFile, "utf8").trim();
};

const networks = fs
  .readdirSync(configurationDirectory, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .filter((network) => fs.existsSync(path.join(configurationDirectory, network, "genesis.vkey")))
  .sort();

const genesisVerificationKeys = Object.fromEntries(
  networks.map((network) => [network, readGenesisVerificationKey(network)]),
);

// Add entry for "dev" mithril networks (mostly spawned using end-to-end).
if (genesisVerificationKeys["dev-preview"] !== undefined) {
  genesisVerificationKeys.dev =
    "5b33322c3235332c3138362c3230312c3137372c31312c3131372c3133352c3138372c3136372c3138312c3138382c32322c35392c3230362c3130352c3233312c3135302c3231352c33302c37382c3231322c37362c31362c3235322c3138302c37322c3133342c3133372c3234372c3136312c36385d";
}

const fileContent = `// **DO NOT EDIT MANUALLY** This file is auto-generated by scripts/generate.genesis-verification-keys.js.

export const genesisVerificationKeys = ${JSON.stringify(genesisVerificationKeys, null, 2)};

export default genesisVerificationKeys;
`;

fs.writeFileSync(outputFile, fileContent);

console.log(
  `Generated ${path.relative(repositoryRoot, outputFile)} with ${
    Object.keys(genesisVerificationKeys).length
  } genesis verification keys`,
);
