// Function used to compute the verification key for production
// It loads a SRS to generate the verification key
pub fn compute_prodution_non_recursive_circuit_verification_key(
    params: &Parameters,
    merkle_tree_depth: u32,
    srs_path: &str,
) -> Vec<u8> {
    let circuit = StmCircuit::try_new(&params, merkle_tree_depth as u32).unwrap();

    // Change the path to the settled path for the SRS
    let srs_file = File::open(srs_path).unwrap();

    let mut reader = BufReader::new(srs_file);
    let srs = ParamsKZG::read_custom(&mut reader, SerdeFormat::RawBytes).unwrap();

    let circuit_verification_key = midnight_zk_stdlib::setup_vk(&srs, &circuit);
    let mut buf_cvk = vec![];
    let _ = circuit_verification_key
        .write(&mut buf_cvk, SerdeFormat::RawBytes)
        .unwrap();
    buf_cvk
}
