// Function used to compute the recursive verification key for production
// It loads a SRS to generate the verification key
pub fn compute_prodution_recursive_circuit_verification_key(srs_path: &str) -> Vec<u8> {
    let shared_srs_degree = 19;

    // Change the path to the settled path for the SRS
    let srs_file = File::open(srs_path).unwrap();

    let mut reader = BufReader::new(srs_file);
    let srs: ParamsKZG<Bls12> = ParamsKZG::read_custom(&mut reader, SerdeFormat::RawBytes).unwrap();

    // Computes the correct size SRS for the recursive circuit
    let mut recursive_commitment_parameters = srs.clone();
    recursive_commitment_parameters.downsize(shared_srs_degree);

    // Loads the non recursive CVK
    let certificate_verifying_key = MidnightVK::read(
        &mut NON_RECURSIVE_CIRCUIT_VERIFICATION_KEY_FOR_PRODUCTION.as_slice(),
        SerdeFormat::RawBytes,
    )
    .unwrap();

    // Create the IVC circuit, it is initiliazed with empty public inputs and the verification key
    // of the non-recursive circuit
    let default_ivc_circuit = IvcCircuit::unknown(certificate_verifying_key.vk());
    let recursive_verifying_key: VerifyingKey<
        <BlstrsEmulation as SelfEmulation>::F,
        KZGCommitmentScheme<<BlstrsEmulation as SelfEmulation>::Engine>,
    > = keygen_vk_with_k(
        &recursive_commitment_parameters,
        &default_ivc_circuit,
        shared_srs_degree,
    )
    .expect("recursive verifying key generation should not fail");

    let mut buf_cvk = vec![];
    let _ = recursive_verifying_key
        .write(&mut buf_cvk, SerdeFormat::RawBytes)
        .unwrap();
    buf_cvk
}
