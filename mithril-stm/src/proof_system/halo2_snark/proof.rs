/// Errors which can be outputted by key registration.
#[derive(Debug, Clone, thiserror::Error, PartialEq, Eq)]
pub enum SnarkError {
    #[error("The proof failed to verify.")]
    VerifyProofFail,
}

/// `SnarkProof` contains a proof generated by the certificate circuit represented as a vector of
/// bytes. It can be verified using verifier parameters derived from the srs, the verification key
/// and the public inputs which are all public values
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SnarkProof {
    /// Raw proof bytes produced by the SNARK prover.
    snark_proof: Vec<u8>,
}

// TODO: remove this allow dead_code directive when function is called or future_snark is activated
#[allow(dead_code)]
impl SnarkProof {
    pub fn new(snark_proof: Vec<u8>) -> Self {
        Self { snark_proof }
    }

    /// Verify a SNARK proof
    /// 1. reconstruct the srs given the STM parameters
    /// 2. regenerate the circuit to compute the verification key
    /// 3. Computes the public input
    /// 4. Runs the verification of the proof
    ///
    /// Step 1. and 2. will be removed once the vk and circuit are fixed
    pub fn verify(
        &self,
        message: &[u8],
        stm_params: &Parameters,
        avk: &AggregateVerificationKey<MithrilMembershipDigest>,
        // I think we need to find a better way to handle the merkle
        // tree depth, probably in some parameters somewhere
        // It is only needed for now to generate the circuit and then the vk
        // So it can be removed once this is handle properly
        merkle_tree_depth: u32,
    ) -> StmResult<()> {
        let k_next_pow2 = stm_params.k.next_power_of_two().trailing_zeros();
        // For now we compute the circuit degree like that but it would
        // be better to have it in some parameters stored somewhere
        let circuit_degree = 11 + k_next_pow2;

        let srs = load_or_generate_srs(
            circuit_degree,
            format!(
                "./src/circuits/halo2/assets/params_kzg_unsafe_{}",
                circuit_degree
            )
            .as_str(),
        )?;
        let verif_params = srs.verifier_params();

        // We need to generate the circuit to be able to derive the vk from the srs
        // This computation should be replaced once the vk is made available to everyone
        let relation = StmCircuit::try_new(stm_params, merkle_tree_depth).unwrap();
        let vk = zk::setup_vk(&srs, &relation);

        let merkle_root = &avk
            .to_snark_aggregate_verification_key()
            .ok_or(anyhow!("Missing avk for SNARK"))?
            .get_merkle_tree_commitment()
            .root;

        let proof_message = build_snark_message(merkle_root.as_slice(), message)?;
        let proof_instance = (proof_message[0].into(), proof_message[1].into());

        let verify_result = zk::verify::<StmCircuit, blake2b_simd::State>(
            &verif_params,
            &vk,
            &proof_instance,
            None,
            &self.snark_proof,
        );

        if verify_result.is_ok() {
            Ok(())
        } else {
            Err(SnarkError::VerifyProofFail.into())
        }
    }

    // TODO: write proper function
    pub fn to_bytes(&self) -> Vec<u8> {
        self.snark_proof.clone()
    }

    /// Deserialise a proof from raw bytes.
    pub fn from_bytes(bytes: Vec<u8>) -> Self {
        Self { snark_proof: bytes }
    }
}

/// Holds the pre-computed SNARK setup and exposes proof generation.
///
/// The type parameter `R` selects the randomness source used during proof generation.
/// Use `try_new_non_deterministic` for production (`OsRng`) and `try_new_deterministic` for
/// reproducible tests.
// TODO: remove this allow dead_code directive when function is called or future_snark is activated
#[allow(dead_code)]
pub struct SnarkProver<R: RngCore + CryptoRng> {
    setup: SnarkSetup,
    rng: R,
}

// TODO: remove this allow dead_code directive when function is called or future_snark is activated
#[allow(dead_code)]
impl SnarkProver<rand_core::OsRng> {
    /// Create a new prover with a non-deterministic randomness source (`OsRng`).
    ///
    /// This is the constructor intended for production use.
    pub fn try_new_non_deterministic(
        parameters: &Parameters,
        merkle_tree_depth: u32,
    ) -> StmResult<Self> {
        Ok(Self {
            setup: SnarkSetup::try_new(parameters, merkle_tree_depth)
                .with_context(|| "Failed to initialize SNARK setup (SRS, circuit, keys)")?,
            rng: rand_core::OsRng,
        })
    }
}

// TODO: remove this allow dead_code directive when function is called or future_snark is activated
#[allow(dead_code)]
impl SnarkProver<ChaCha20Rng> {
    /// Create a new prover with a deterministic randomness source seeded from `seed`.
    ///
    /// This constructor is intended for testing, where reproducible proof generation is needed.
    pub fn try_new_deterministic(
        parameters: &Parameters,
        merkle_tree_depth: u32,
        seed: [u8; 32],
    ) -> StmResult<Self> {
        Ok(Self {
            setup: SnarkSetup::try_new(parameters, merkle_tree_depth)
                .with_context(|| "Failed to initialize SNARK setup (SRS, circuit, keys)")?,
            rng: ChaCha20Rng::from_seed(seed),
        })
    }
}

// TODO: remove this allow dead_code directive when function is called or future_snark is activated
#[allow(dead_code)]
impl<R: RngCore + CryptoRng> SnarkProver<R> {
    /// Aggregate a set of single signatures into a SNARK proof.
    ///
    /// Prepares the prover input (public instance and per-index witness) from the collected
    /// single signatures, then runs the SNARK prover.
    ///
    /// Returns an error if fewer than `k` valid signatures are available or if proof generation fails.
    pub fn aggregate_signatures<D: MembershipDigest>(
        &mut self,
        clerk: &SnarkClerk,
        signatures: &[SingleSignature],
        message: &[u8],
    ) -> StmResult<SnarkProof> {
        let snark_prover_input =
            SnarkProverInput::prepare_prover_input::<D>(clerk, signatures, message)
                .with_context(|| "Failed to prepare SNARK prover input")?;
        let instance = snark_prover_input.get_instance();
        let witness = snark_prover_input.into_witness();

        let snark_proof = zk::prove::<StmCircuit, PoseidonState<CircuitBase>>(
            &self.setup.srs,
            &self.setup.proving_key,
            &self.setup.circuit,
            &instance,
            witness,
            &mut self.rng,
        )
        .with_context(|| "SNARK proof generation failed")?;

        Ok(SnarkProof { snark_proof })
    }
}

#[cfg(feature = "future_snark")]
#[cfg(test)]
mod tests {
    use rand_chacha::ChaCha20Rng;
    use rand_core::SeedableRng;

    use crate::{
        Initializer, KeyRegistration, MithrilMembershipDigest, Parameters, Signer, SingleSignature,
        proof_system::SnarkClerk,
    };

    use super::SnarkProver;

    type D = MithrilMembershipDigest;

    fn setup_signers_and_clerk(
        params: Parameters,
        nparties: usize,
        rng: &mut ChaCha20Rng,
    ) -> (Vec<Signer<D>>, SnarkClerk) {
        let mut key_reg = KeyRegistration::initialize();
        let mut initializers = Vec::with_capacity(nparties);

        for _ in 0..nparties {
            let init = Initializer::new(params, 1, rng);
            key_reg.register_by_entry(&init.clone().try_into().unwrap()).unwrap();
            initializers.push(init);
        }

        let closed_reg = key_reg.close_registration(&params).unwrap();
        let signers: Vec<Signer<D>> = initializers
            .into_iter()
            .map(|init| init.try_create_signer::<D>(&closed_reg).unwrap())
            .collect();

        let clerk = SnarkClerk::new_clerk_from_signer(&signers[0]);
        (signers, clerk)
    }

    fn collect_signatures(signers: &[Signer<D>], message: &[u8]) -> Vec<SingleSignature> {
        signers
            .iter()
            .filter_map(|signer| signer.create_single_signature(message).ok())
            .collect()
    }

    fn create_prover(
        params: Parameters,
        number_of_signers: usize,
        seed: [u8; 32],
    ) -> SnarkProver<ChaCha20Rng> {
        let merkle_tree_depth = number_of_signers.next_power_of_two().trailing_zeros();
        SnarkProver::try_new_deterministic(&params, merkle_tree_depth, seed).unwrap()
    }

    #[test]
    fn produces_valid_snark_proof() {
        let mut rng = ChaCha20Rng::from_seed([0u8; 32]);
        let params = Parameters {
            m: 200,
            k: 5,
            phi_f: 0.8,
        };
        let nparties = 10;
        let message = [1u8; 32];

        let (signers, clerk) = setup_signers_and_clerk(params, nparties, &mut rng);
        let signatures = collect_signatures(&signers, &message);
        let mut prover = create_prover(
            params,
            clerk.closed_key_registration.number_of_registered_parties(),
            [0u8; 32],
        );

        let result = prover.aggregate_signatures::<D>(&clerk, &signatures, &message);
        assert!(
            result.is_ok(),
            "Expected proof creation to succeed, got: {result:?}"
        );

        let proof = result.unwrap();
        assert!(
            !proof.snark_proof.is_empty(),
            "Proof bytes should not be empty"
        );
    }

    #[test]
    fn fails_with_insufficient_signatures() {
        let mut rng = ChaCha20Rng::from_seed([1u8; 32]);
        let params = Parameters {
            m: 20,
            k: 5,
            phi_f: 0.1,
        };
        let nparties = 4;
        let message = [2u8; 32];

        let (signers, clerk) = setup_signers_and_clerk(params, nparties, &mut rng);
        let signatures = collect_signatures(&signers, &message);
        let mut prover = create_prover(
            params,
            clerk.closed_key_registration.number_of_registered_parties(),
            [1u8; 32],
        );

        let result = prover.aggregate_signatures::<D>(&clerk, &signatures, &message);
        assert!(
            result.is_err(),
            "Expected failure due to insufficient signatures"
        );
    }

    #[test]
    fn fails_with_empty_signatures() {
        let mut rng = ChaCha20Rng::from_seed([2u8; 32]);
        let params = Parameters {
            m: 200,
            k: 5,
            phi_f: 0.8,
        };
        let nparties = 5;

        let (_, clerk) = setup_signers_and_clerk(params, nparties, &mut rng);
        let mut prover = create_prover(
            params,
            clerk.closed_key_registration.number_of_registered_parties(),
            [2u8; 32],
        );

        let result = prover.aggregate_signatures::<D>(&clerk, &[], &[3u8; 32]);
        assert!(result.is_err(), "Expected failure with empty signatures");
    }
}

// Temporary function to load a srs given a path or the generate it
// given a circuit degree, the generation can be relatively slow for degrees
// greater than 13 or 14 and the result is not saved on file
// This function should be removed when proper handling of the srs is created
#[allow(dead_code)]
fn load_or_generate_srs(circuit_degree: u32, path: &str) -> StmResult<ParamsKZG<Bls12>> {
    let file = File::open(path);
    match file {
        Ok(f) => {
            let mut reader = BufReader::new(f);
            Ok(
                ParamsKZG::read_custom(&mut reader, SerdeFormat::RawBytesUnchecked)
                    .with_context(|| "Failed to read the srs bytes.")?,
            )
        }
        Err(_) => Ok(ParamsKZG::unsafe_setup(
            circuit_degree,
            ChaCha20Rng::seed_from_u64(42),
        )),
    }
}

